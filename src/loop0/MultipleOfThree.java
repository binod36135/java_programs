package loop0;
public class MultipleOfThree {
    public static void main(String[] args) {
        System.out.println("The multiple of 3 is");
        int multiple=3;
        for(int i=1;i<=10;i++){
            System.out.println(multiple+"X"+i+"="+multiple*i);
        }
    }
    
}
