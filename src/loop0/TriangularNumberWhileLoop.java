package loop0;
public class TriangularNumberWhileLoop {
    public static void main(String[] args) {
        int firstNumber=0;
        int i=1;
        while(i<=9){
            firstNumber=firstNumber+i;
            System.out.println(firstNumber);
            i++;
        }
    }
    
}
