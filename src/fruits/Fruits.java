package fruits;
public class Fruits {
    private String colour;
    private String shape;
    private int cost;
    public int getCost(){
        return cost;
    }
    public void setCost(int cost){
        this.cost=cost;
    }
    public String getShape(){
        return shape;
    }
    public void setShape(String shape){
        this.shape=shape;
    }
    public String getColour(){
        return colour;
    }
    public void setColour(String colour){
        this.colour=colour;
        
    }
   
 
}
